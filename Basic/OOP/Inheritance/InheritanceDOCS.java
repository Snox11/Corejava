package Basic.OOP.Inheritance;

public class InheritanceDOCS {
/*
 * -------Inheritance------
 * #used for code reusability
 * #Process of accesssing properties and methods of parent/super/base class to child/sub/derived class is known as inheritance.
 * 
 * # Also known as IS-A relationship.
 * # Its a Generalization to Specilization process.
 * # We can inherit only one class at a time.
 * # We can't inherit private properties,methods and constructors.
 * # "extends" keyword is used in inheritance.
 * # Mutiple inheritance is not supported in java in class level but supports in interface. 
 * 
 * #syntax :
 * child class extends parent_class{
 * //properties : parent + child 
 * //methods    : parent + child
 * }
 * e.g
 * class A{
 * }
 * class B extends A{
 * }
 * class C extends B{
 * }
 */
}
